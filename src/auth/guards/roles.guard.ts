import { CanActivate, ExecutionContext, Injectable } from '@nestjs/common';
import { Reflector } from '@nestjs/core';
import { Role } from '@prisma/client';
import { ROLES_KEY } from '../decorators/roles.decorator';

@Injectable()
export class RolesGuard implements CanActivate {
  constructor(private reflector: Reflector) {}

  canActivate(context: ExecutionContext): boolean {
    const requiredRoles = this.reflector.getAllAndOverride<Role[]>(ROLES_KEY, [
      context.getHandler(),
      context.getClass(),
    ]);
    
    if (!requiredRoles) {
      return true;
    }
    
    const { user } = context.switchToHttp().getRequest();
    
    if (!user) {
      return false;
    }
    
    // Check if user's role matches any of the required roles
    // Prisma enum values: Role.Admin = "Admin" (capitalized enum name)
    // Database stores: "admin" (lowercase via @map)
    // Handle both enum name (capitalized) and enum value (lowercase) formats
    const userRole = user.roles;
    const userRoleStr = String(userRole).toLowerCase();
    
    return requiredRoles.some((role) => {
      const roleStr = String(role).toLowerCase();
      // Compare both as enum values and as normalized lowercase strings
      return userRole === role || userRoleStr === roleStr;
    });
  }
} 